 public class Match {

        private int matchId;
        private int year;
        private String winner;

     public int getMatchId() {
         return matchId;
     }

     public void setMatchId(int matchId) {
         this.matchId = matchId;
     }

     public int getYear() {
         return year;
     }

     public void setYear(int year) {
         this.year = year;
     }

     public String getWinner() {
         return winner;
     }

     public void setWinner(String winner) {
         this.winner = winner;
     }
 }
